package edu.diego.preciado.act_2;

public class Suma {
    /**
     * Este metodo recibe dos operandos enteros y devuelve su suma
     * @return la suma de op1 y op2
     * @param op1 primer operando
     * @param op2 segundo operando
     */
    public static int realizarOperacion(int op1, int op2){
        return op1 + op2;
    }
}

